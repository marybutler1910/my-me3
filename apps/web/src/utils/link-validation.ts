/**
 * Sanitize user input by stripping URLs and cleaning up the value
 */
export function sanitizeInput(platformKey: string, value: string): string {
  const platform = getPlatformConfig(platformKey);
  if (!platform) return value.trim();

  let cleaned = value.trim();

  if (platformKey === "substack") {
    const m = cleaned.match(
      /^(?:https?:\/\/)?(?:www\.)?([^.]+)\.substack\.com\/?$/i,
    );
    if (m) cleaned = m[1];
    cleaned = cleaned.replace(/^@/, "");
    cleaned = cleaned.replace(/\/+$/, "");
    return cleaned;
  }

  // Apply URL patterns to strip prefixes
  for (const pattern of platform.urlPatterns) {
    cleaned = cleaned.replace(pattern, "");
  }

  // Remove trailing slashes
  cleaned = cleaned.replace(/\/+$/, "");

  // Remove leading @ for username-based platforms
  if (platform.validate === "username") {
    cleaned = cleaned.replace(/^@/, "");
  }

  // For phone numbers, keep only digits and +
  if (platform.validate === "phone") {
    cleaned = cleaned.replace(/[^0-9+]/g, "");
  }

  return cleaned;
}

/**
 * Validate a value for a specific platform
 */
export function validatePlatformValue(
  platformKey: string,
  value: string,
): { valid: boolean; error?: string } {
  const platform = getPlatformConfig(platformKey);
  if (!platform) return { valid: true };

  const schema = getValidationSchema(platform.validate);
  const result = schema.safeParse(value);

  if (result.success) {
    return { valid: true };
  }

  return {
    valid: false,
    error: result.error.issues[0]?.message || "Invalid value",
  };
}

/**
 * Detect platform from a pasted URL
 */
export function detectPlatformFromUrl(url: string): string | null {
  const lowerUrl = url.toLowerCase();

  // Check each platform's patterns
  for (const platform of ALL_PLATFORMS) {
    for (const pattern of platform.urlPatterns) {
      if (pattern.test(url)) {
        return platform.key;
      }
    }
  }

  // Fallback domain matching
  const domainMatches: Record<string, string> = {
    "twitter.com": "twitter",
    "x.com": "twitter",
    "instagram.com": "instagram",
    "tiktok.com": "tiktok",
    "youtube.com": "youtube",
    "youtu.be": "youtube",
    "linkedin.com": "linkedin",
    "github.com": "github",
    "discord.gg": "discord",
    "discord.com": "discord",
    "twitch.tv": "twitch",
    "threads.net": "threads",
    "bsky.app": "bluesky",
    "snapchat.com": "snapchat",
    "t.me": "telegram",
    "telegram.me": "telegram",
    "wa.me": "whatsapp",
    "whatsapp.com": "whatsapp",
    "spotify.com": "spotify",
    "soundcloud.com": "soundcloud",
    "music.apple.com": "applemusic",
    "medium.com": "medium",
    "substack.com": "substack",
    "dribbble.com": "dribbble",
    "behance.net": "behance",
    "pinterest.com": "pinterest",
  };

  for (const [domain, platformKey] of Object.entries(domainMatches)) {
    if (lowerUrl.includes(domain)) {
      return platformKey;
    }
  }

  return null;
}

/**
 * Build the final URL for a platform link
 */
export function buildPlatformUrl(platformKey: string, value: string): string {
  const platform = getPlatformConfig(platformKey);
  if (!platform) return value;

  const sanitized = sanitizeInput(platformKey, value);
  return platform.buildUrl(sanitized);
}

